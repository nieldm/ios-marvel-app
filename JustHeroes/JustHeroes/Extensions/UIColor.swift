import Foundation
import UIKit

extension UIColor {
    static let primary: UIColor = UIColor(named: "Primary") ?? .white
    static let secondary: UIColor = UIColor(named: "Secondary") ?? .black
    static let offwhite: UIColor = UIColor(named: "OffWhite") ?? .white
    static let darkb: UIColor = UIColor(named: "DarkB") ?? .black
    static let cellBackground: UIColor = UIColor(named: "CellBackground") ?? .white
}
